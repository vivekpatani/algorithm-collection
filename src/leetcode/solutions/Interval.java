package leetcode.solutions;

/**
 * Class Interval
 * @author flipswitch
 *
 */
public class Interval {
	
	int start;
	int end;

	/**
	 * Constructor
	 */
	Interval() {
		start = 0;
		end = 0;
	}

	/**
	 * Constructor
	 * @param s
	 * @param e
	 */
	Interval(int s, int e) {
		start = s;
		end = e;
	}
}